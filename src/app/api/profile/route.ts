import { NextRequest, NextResponse } from "next/server";
import { createClient } from "@/lib/supabase/server";

export async function GET() {
  const supabase = await createClient();
  const {
    data: { user },
  } = await supabase.auth.getUser();

  if (!user) {
    return NextResponse.json({ error: "Unauthorized" }, { status: 401 });
  }

  const { data, error } = await supabase
    .from("profiles")
    .select("*")
    .eq("id", user.id)
    .single();

  if (error) {
    return NextResponse.json({ error: error.message }, { status: 500 });
  }

  return NextResponse.json(data);
}

export async function PUT(req: NextRequest) {
  const supabase = await createClient();
  const {
    data: { user },
  } = await supabase.auth.getUser();

  if (!user) {
    return NextResponse.json({ error: "Unauthorized" }, { status: 401 });
  }

  const body = await req.json();
  const updates: Record<string, unknown> = {};

  if (body.display_name !== undefined) {
    updates.display_name = body.display_name;
  }

  if (body.buyer_profile !== undefined) {
    updates.buyer_profile = body.buyer_profile;
    // Increment buyer_profile_version
    const { data: currentProfile } = await supabase
      .from("profiles")
      .select("buyer_profile_version")
      .eq("id", user.id)
      .single();

    updates.buyer_profile_version =
      (currentProfile?.buyer_profile_version || 0) + 1;
  }

  if (body.kumo_filters !== undefined) {
    updates.kumo_filters = body.kumo_filters;
  }

  const { data, error } = await supabase
    .from("profiles")
    .update(updates)
    .eq("id", user.id)
    .select()
    .single();

  if (error) {
    return NextResponse.json({ error: error.message }, { status: 500 });
  }

  return NextResponse.json(data);
}
